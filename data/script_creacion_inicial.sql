  FOREIGN KEY ([COMPRA_PRODUCTO_COD], [COMPRA_PRODUCTO_TIPO]) REFERENCES [NO_HAY_BACKUP].PRODUCTO_OPERACION([PRODUCTO_CODIGO],[PRODUCTO_TIPO]),
  CONSTRAINT ck_compra_precio CHECK ([COMPRA_PRECIO] >= 0),
  CONSTRAINT ck_compra_cant CHECK ([COMPRA_CANT] >= 0)
);

	