  [AUTO_CODIGO] decimal(18,0) IDENTITY (1,1),
  [AUTO_NRO_MOTOR] nvarchar(50) NOT NULL,
  [AUTO_NRO_CHASIS] nvarchar(50) NOT NULL,
  [AUTO_PATENTE] nvarchar(50),
  [AUTO_CANT_KMS] decimal(18,0) DEFAULT 0,
